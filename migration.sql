CREATE TABLE climate (
    climate_id char(10) NOT NULL,
    name character varying(100) NOT NULL,
    temperature_range character varying(50) NOT NULL,
    rainfall_range character varying(50) NOT NULL,
    humidity_range character varying(50) NOT NULL,
    CONSTRAINT "PK_climate" PRIMARY KEY (climate_id)
);

CREATE TABLE plant_type (
    plant_type_id char(10) NOT NULL,
    type_name character varying(100) NOT NULL,
    description character varying(255) NOT NULL,
    CONSTRAINT "PK_plant_type" PRIMARY KEY (plant_type_id)
);

CREATE TABLE region (
    region_id char(10) NOT NULL,
    name character varying(100) NOT NULL,
    note character varying(255) NOT NULL,
    CONSTRAINT "PK_region" PRIMARY KEY (region_id)
);

CREATE TABLE soil_type (
    soil_type_id char(10) NOT NULL,
    name character varying(100) NOT NULL,
    note character varying(255) NOT NULL,
    CONSTRAINT "PK_soil_type" PRIMARY KEY (soil_type_id)
);

CREATE TABLE usage (
    usage_id char(10) NOT NULL,
    name character varying(100) NOT NULL,
    note character varying(255) NOT NULL,
    CONSTRAINT "PK_usage" PRIMARY KEY (usage_id)
);

CREATE TABLE user_account (
    user_id integer GENERATED BY DEFAULT AS IDENTITY,
    last_name character varying(255) NOT NULL,
    gender char(1) NOT NULL,
    date_of_birth timestamp with time zone NOT NULL,
    avatar_url text NOT NULL,
    CONSTRAINT "PK_user_account" PRIMARY KEY (user_id)
);

CREATE TABLE plant_info (
    plant_id char(10) NOT NULL,
    scientific_name character varying(255) NOT NULL,
    common_name character varying(255) NOT NULL,
    description text NOT NULL,
    plant_type_id char(10) NOT NULL,
    created_date timestamp with time zone,
    updated_date timestamp with time zone,
    harvest_date_days integer,
    CONSTRAINT "PK_plant_info" PRIMARY KEY (plant_id),
    CONSTRAINT "FK_plant_info_plant_type_plant_type_id" FOREIGN KEY (plant_type_id) REFERENCES plant_type (plant_type_id) ON DELETE CASCADE
);

CREATE TABLE user_login_data (
    user_id integer NOT NULL,
    username character varying(100) NOT NULL,
    password_salt character varying(100) NOT NULL,
    password_hash character varying(250) NOT NULL,
    created_at timestamp with time zone NOT NULL,
    last_login_at timestamp with time zone,
    CONSTRAINT "PK_user_login_data" PRIMARY KEY (user_id),
    CONSTRAINT "FK_user_login_data_user_account_user_id" FOREIGN KEY (user_id) REFERENCES user_account (user_id) ON DELETE CASCADE
);

CREATE TABLE plant_care (
    plant_id char(10) NOT NULL,
    light_requirement integer NOT NULL,
    watering_needs integer NOT NULL,
    humidity_preference integer,
    growth_rate integer,
    soil_recommendation text NOT NULL,
    fertilizer_info text NOT NULL,
    CONSTRAINT "PK_plant_care" PRIMARY KEY (plant_id),
    CONSTRAINT "FK_plant_care_plant_info_plant_id" FOREIGN KEY (plant_id) REFERENCES plant_info (plant_id) ON DELETE CASCADE
);

CREATE TABLE plant_climate (
    plant_id char(10) NOT NULL,
    climate_id char(10) NOT NULL,
    CONSTRAINT "PK_plant_climate" PRIMARY KEY (plant_id, climate_id),
    CONSTRAINT "FK_plant_climate_climate_climate_id" FOREIGN KEY (climate_id) REFERENCES climate (climate_id) ON DELETE CASCADE,
    CONSTRAINT "FK_plant_climate_plant_info_plant_id" FOREIGN KEY (plant_id) REFERENCES plant_info (plant_id) ON DELETE CASCADE
);

CREATE TABLE plant_img (
    image_id char(10) NOT NULL,
    plant_id char(10) NOT NULL,
    image_url text NOT NULL,
    caption character varying(255) NOT NULL,
    CONSTRAINT "PK_plant_img" PRIMARY KEY (image_id),
    CONSTRAINT "FK_plant_img_plant_info_plant_id" FOREIGN KEY (plant_id) REFERENCES plant_info (plant_id) ON DELETE CASCADE
);

CREATE TABLE plant_region (
    plant_id char(10) NOT NULL,
    region_id char(10) NOT NULL,
    CONSTRAINT "PK_plant_region" PRIMARY KEY (plant_id, region_id),
    CONSTRAINT "FK_plant_region_plant_info_plant_id" FOREIGN KEY (plant_id) REFERENCES plant_info (plant_id) ON DELETE CASCADE,
    CONSTRAINT "FK_plant_region_region_region_id" FOREIGN KEY (region_id) REFERENCES region (region_id) ON DELETE CASCADE
);

CREATE TABLE plant_soil (
    plant_id char(10) NOT NULL,
    soil_type_id char(10) NOT NULL,
    CONSTRAINT "PK_plant_soil" PRIMARY KEY (plant_id, soil_type_id),
    CONSTRAINT "FK_plant_soil_plant_info_plant_id" FOREIGN KEY (plant_id) REFERENCES plant_info (plant_id) ON DELETE CASCADE,
    CONSTRAINT "FK_plant_soil_soil_type_soil_type_id" FOREIGN KEY (soil_type_id) REFERENCES soil_type (soil_type_id) ON DELETE CASCADE
);

CREATE TABLE plant_usage (
    plant_id char(10) NOT NULL,
    usage_id char(10) NOT NULL,
    CONSTRAINT "PK_plant_usage" PRIMARY KEY (plant_id, usage_id),
    CONSTRAINT "FK_plant_usage_plant_info_plant_id" FOREIGN KEY (plant_id) REFERENCES plant_info (plant_id) ON DELETE CASCADE,
    CONSTRAINT "FK_plant_usage_usage_usage_id" FOREIGN KEY (usage_id) REFERENCES usage (usage_id) ON DELETE CASCADE
);

